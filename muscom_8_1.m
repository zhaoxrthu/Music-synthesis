clear all, close all, clc
load('guitar.mat');
N=length(wave2proc);
times=10;
T=N/8000;
OMG=8000*2*pi;
wave=repmat(wave2proc,times,1);
[t,omg,FT,IFT]=prefourier([0,T],N,[-OMG/2,OMG/2],8000);
F1=FT*wave2proc;
[t,omg,FT,IFT]=prefourier([0,T*times],times*N,[-OMG/2,OMG/2],8000);
F2=FT*wave;
figure;
hold on;
subplot(2,2,1);
plot(wave2proc);
axis([0 250 -0.15 0.2]);
title('wave2proc');
subplot(2,2,2);
plot(wave);
axis([0 2500 -0.15 0.2]);
title('wave2proc by 10 times');
subplot(2,2,3);
plot(abs(F1(4001:8000)));
axis([0 3500 0 0.0015]);
title('Fourier Transform');
subplot(2,2,4);
plot(abs(F2(4001:8000)));
axis([0 3500 0 0.015]);
title('Fourier Transform');